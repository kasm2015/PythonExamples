#IDENTITY OPERATORS
a = 10
b = 12

if(a is b):
    print(" a is present in b")
else:
    print("a is not present in b")
c = 10
d = 10
if(c is d):
    print("c is present in d")
else:
    print("c is not present in d")

m = 19
n = 29
if(m is not n):
    print("m is not present in n")
else:
    print("m is present in n")