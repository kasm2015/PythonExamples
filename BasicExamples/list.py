#DEFINE A LIST
list1 = ['Mango','Apple','Orange','Banand']

#PRINT A LIST
print("Printing a list --> ",list1)

#UPDATE A LIST
list1[1]='Pineapple'
print("List after updating --> ",list1)

#PRINT LENGTH OF LIST
print("Length of list --> ",len(list1))

#PRINT AN ELEMENT OF A LIST
print("PRINT FIRST ELEMENT --> ",list1[0])

#PRINT 3 elements of a list
print("Print 3 elements of a list --> ",list1[0:3])

#CONCATENATE TWO LIST
list2=['Tomato','Jackfruit']

print("Concatenate two list --> ",list1+list2)

#PRINT LIST MULTIPLE TIMES
print("Printing list multiple times --> ",list1*2)